// Create an object called family with a few key value pairs.
let family = {
    mama: "Elena",
    papa: "Mikhail",
    me: "Ekaterina"
}
// Using a for in loop, console.log the keys of the object.
for(key in family){
    console.log(key)
}
// Using a for in loop, console.log the values of the object.
for(key in family){
    console.log(family[key])
}